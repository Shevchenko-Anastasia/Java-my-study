package net.study.basic.methods;

public class Calculator {



    public static int sum(int digit1, int digit2){
        int result = digit1 + digit2;
        return result;
    };

    public static int multiply(int digit1, int digit2){
        int result = digit1 * digit2;
        return result;
    };

    public static float divide(float digit1, float digit2){
        float result = digit1 / digit2;
        return result;
    }
}
