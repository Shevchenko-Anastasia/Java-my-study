package net.study.basic.lesson2;

class PrimitiveTypes {

    public static void main(String[] args) {

        byte varMyByte = -126;
        short varMyShort = 32767;
        int varMyInt = 2147483647;
        long varMyLong = 9223372036854775807L;
        float varMyFloat = 3.69F;
        double varMyDouble = 12.3456789012345D;
        boolean varMyBoolean = false;
        char varMyChar = 'A';

        System.out.println ("varMyByte = " + varMyByte);
        System.out.println("varMyShort = " + varMyShort);
        System.out.println("varMyInt = " + varMyInt);
        System.out.println("varMyLong = " + varMyLong);
        System.out.println("varMyFloat = " + varMyFloat);
        System.out.println("varMyDouble = " + varMyDouble);
        System.out.println("varMyBoolean = " + varMyBoolean);
        System.out.println("varMyChar = " + varMyChar);
    }

}
